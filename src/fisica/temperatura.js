// Celsius para Fahrenheit
const CtoF = (celsius) => (celsius * 1.8  + 32)

// Celsius para Kelvin
const CtoK = (celsius) => (celsius + 273.15)

// Fahrenheit para Celsius
const FtoC = (fahrenheit) => (fahrenheit - 32 ) / 1.8

// Fahrenheit para Kelvin
const FtoC = (fahrenheit) => (fahrenheit * 1.8) - 459.67

// Kelvin para Celsius
const KtoC = (kelvin) => (kelvin - 273.15)

// Kelvin para Fahrenheit
const KtoF = (kelvin) => (kelvin * 1.8) - 459.67
